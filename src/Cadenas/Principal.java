    private void escribirF(){
        try(FileWriter fw = new FileWriter(this.ficheroActual)){
                    
                    fw.write(this.contenido.getText());
                } catch (IOException ex) {
                    Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                }
    }
    
    private void AbrirVentanaGuardar(){
        JFileChooser fc = new JFileChooser();
            fc.setMultiSelectionEnabled(false);
            fc.setFileSelectionMode(JFileChooser.FILES_ONLY );
            int seleccion = fc.showSaveDialog(this.getContentPane());
            
            if (seleccion == JFileChooser.APPROVE_OPTION  ){
                
                this.ficheroActual = fc.getSelectedFile();
                this.escribirF();
                
            }
    }
    
    private void Metodostxt(){
        
             dato = this.contenido.getText();
        String[] valor = this.contenido.getText().split(" ");
        String[] variable = dato.split("");
        
        int uno = 0;
        int dos = 0;
     
        String longitudtxt = this.contenido.getText();
       int tamano = longitudtxt.length();
       StringTokenizer st = new StringTokenizer(longitudtxt);
       
       String letras=" ";
       
       int ultima_letra;
       int mitad_letra=0;
       
       for (int i =0; i < longitudtxt.length(); i++){
       
           if (Character.isLetter(longitudtxt.charAt(i))){
            mitad_letra++;
            letras+=longitudtxt.charAt(i);
            
            
           }
          
           
       }
       

       ultima_letra = longitudtxt.length();
       
       

 
       this.longitud.setText(""+tamano+" caracteres" );
       this.num_palabras.setText(""+st.countTokens());
       this.primer.setText("letra: "+longitudtxt.charAt(0));

      // this.letra_central.setText("letra: "+longitudtxt.charAt((mitad_letra-1)/2 ));
       
       
       
       /////////////////////////
       
         
    
        for (int i = 0; i < valor.length; i++){
            this.primera_palabra.setText(valor[0]);
            break;
        }
        
        for (int i = 0; i < variable.length; i++){
            uno ++;
        }
        
       
        
        for (int i = 0; i < valor.length; i++){
            dos ++;
        }
        
        this.ultima_letra.setText(variable[uno-1]);
        
        
        int mitad = uno / 2;
        this.letra_central.setText(variable[mitad]);
        
        
        int p_mitad = dos / 2;
        this.palabra_central.setText(valor[p_mitad]);
        
        
        this.ultima_palabra.setText(valor[dos-1]);
        
        // contar letras
 
        new Contador().Ra(dato,"a");
        new Contador().Re(dato,"e");
        new Contador().Ri(dato,"i");
        new Contador().Ru(dato,"u");
        new Contador().Ro(dato,"o");
        
        new Contador().par_o_impar(dato);
        
        //traducion
        
        new Traductor().T_R(dato);
        
    }

    
    
